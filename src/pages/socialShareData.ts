/**
 * Text and image that display when this page is shared on social networks.
 */
export interface SocialShareData {
    /**
     * Image source key.
     */
    imageSourceKey?: string;

    /**
     * Image width.
     */
    imageWidth?: string;

    /**
     * Image height.
     */
    imageHeight?: string;

    /**
     * Page title on social networks.
     */
    title?: string;
}